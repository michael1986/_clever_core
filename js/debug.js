function hideDebugWindow() {
    document.getElementById("DebugWindowContainer").style.display="none";
    document.getElementById("DebugShowBtn").style.display="block";
    document.getElementById("DebugWindowContainerSmall").style.display="block";
}
function showDebugWindow() {
    document.getElementById("DebugWindowContainer").style.display="block";
    document.getElementById("DebugShowBtn").style.display="none";
    document.getElementById("DebugWindowContainerSmall").style.display="none";
}
